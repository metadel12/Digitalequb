/**
 * Translation function with interpolation and pluralization support
 * @param {string} key - Translation key (e.g., 'common.welcome')
 * @param {Object} params - Parameters for interpolation
 * @param {number} count - Count for pluralization
 * @returns {string} Translated text
 */
export const t = (key, params = {}, count = null) => {
    // Get current language from localStorage or default to 'en'
    const currentLang = localStorage.getItem('app-language') || 'en';

    // Get translation object
    const translation = translations[currentLang] || translations.en;

    // Get nested value by key
    const keys = key.split('.');
    let value = translation;
    for (const k of keys) {
        if (value && typeof value === 'object') {
            value = value[k];
        } else {
            value = undefined;
            break;
        }
    }

    // Fallback to English if not found
    if (!value && currentLang !== 'en') {
        const enTranslation = translations.en;
        let enValue = enTranslation;
        for (const k of keys) {
            if (enValue && typeof enValue === 'object') {
                enValue = enValue[k];
            } else {
                enValue = undefined;
                break;
            }
        }
        value = enValue || key;
    }

    if (!value) return key;

    // Handle pluralization
    if (count !== null) {
        const pluralRules = getPluralRules(currentLang);
        const pluralForm = pluralRules(count);
        if (typeof value === 'object') {
            value = value[pluralForm] || value.other || value;
        }
    }

    // Handle interpolation
    if (typeof value === 'string' && params) {
        Object.keys(params).forEach(param => {
            value = value.replace(new RegExp(`{{${param}}}`, 'g'), params[param]);
        });
    }

    return value;
};

/**
 * Get pluralization rules for language
 * @param {string} lang - Language code
 * @returns {Function} Pluralization function
 */
export const getPluralRules = (lang) => {
    const rules = {
        // English: 1 = singular, other = plural
        en: (n) => n === 1 ? 'one' : 'other',
        // Amharic: 1 = singular, other = plural
        am: (n) => n === 1 ? 'one' : 'other',
        // Arabic: 0, 1, 2, few, many, other
        ar: (n) => {
            if (n === 0) return 'zero';
            if (n === 1) return 'one';
            if (n === 2) return 'two';
            if (n % 100 >= 3 && n % 100 <= 10) return 'few';
            if (n % 100 >= 11 && n % 100 <= 99) return 'many';
            return 'other';
        },
        // French: 0, 1 = singular, other = plural
        fr: (n) => n === 0 || n === 1 ? 'one' : 'other',
        // Spanish: 1 = singular, other = plural
        es: (n) => n === 1 ? 'one' : 'other',
        // German: 1 = singular, other = plural
        de: (n) => n === 1 ? 'one' : 'other',
        // Russian: 1, 2-4, 5+
        ru: (n) => {
            if (n % 10 === 1 && n % 100 !== 11) return 'one';
            if (n % 10 >= 2 && n % 10 <= 4 && (n % 100 < 10 || n % 100 >= 20)) return 'few';
            return 'many';
        },
    };
    return rules[lang] || rules.en;
};

