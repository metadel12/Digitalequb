// ==================== TRANSLATION UTILITIES ====================

/**
 * Get available languages
 * @returns {Array} List of available languages
 */
export const getAvailableLanguages = () => {
    return Object.keys(translations).map(code => ({
        code,
        name: SUPPORTED_LANGUAGES[code]?.name || code,
        nativeName: SUPPORTED_LANGUAGES[code]?.nativeName || code,
        flag: SUPPORTED_LANGUAGES[code]?.flag || '🌐',
        direction: SUPPORTED_LANGUAGES[code]?.direction || 'ltr',
    }));
};

/**
 * Get current language
 * @returns {string} Current language code
 */
export const getCurrentLanguage = () => {
    return localStorage.getItem('app-language') || 'en';
};

/**
 * Set current language
 * @param {string} lang - Language code
 */
export const setCurrentLanguage = (lang) => {
    if (translations[lang]) {
        localStorage.setItem('app-language', lang);
        document.documentElement.lang = lang;
        document.documentElement.dir = SUPPORTED_LANGUAGES[lang]?.direction || 'ltr';
        window.dispatchEvent(new CustomEvent('languageChange', { detail: { language: lang } }));
    }
};

/**
 * Detect browser language
 * @returns {string} Detected language code
 */
export const detectBrowserLanguage = () => {
    const browserLang = navigator.language.split('-')[0];
    return translations[browserLang] ? browserLang : 'en';
};

/**
 * Initialize language
 */
export const initLanguage = () => {
    let lang = localStorage.getItem('app-language');
    if (!lang) {
        lang = detectBrowserLanguage();
    }
    setCurrentLanguage(lang);
    return lang;
};

